// Type guard to check if a recipe is a full object vs just an ID
export function isRecipeObject(recipe: Recipe | string): recipe is Recipe {
  return typeof recipe === 'object' && recipe !== null && ('name' in recipe);
}

// Type guard to check if recipes array contains Recipe objects or string IDs
export function hasRecipeObjects(recipes: (Recipe | string)[]): recipes is Recipe[] {
  return recipes.length > 0 && typeof recipes[0] !== 'string';
}

